package com.framework.support;

public class ReportTheme {

	private String headingBackColor;
	private String headingForeColor;
	private String selectionBackColor;
	private String selectionForeColor;
	private String contentBackColor;
	private String contentForeColor;

	public String getHeadingBackColor() {
		return this.headingBackColor;
	}

	public void setHeadingBackColor(final String headingBackColor) {
		this.headingBackColor = headingBackColor;
	}

	public String getHeadingForeColor() {
		return this.headingForeColor;
	}

	public void setHeadingForeColor(final String headingForeColor) {
		this.headingForeColor = headingForeColor;
	}

	public String getSelectionBackColor() {
		return this.selectionBackColor;
	}

	public void setSelectionBackColor(final String selectionBackColor) {
		this.selectionBackColor = selectionBackColor;
	}

	public String getSelectionForeColor() {
		return this.selectionForeColor;
	}

	public void setSelectionForeColor(final String selectionForeColor) {
		this.selectionForeColor = selectionForeColor;
	}

	public String getContentBackColor() {
		return this.contentBackColor;
	}

	public void setContentBackColor(final String contentBackColor) {
		this.contentBackColor = contentBackColor;
	}

	public String getContentForeColor() {
		return this.contentForeColor;
	}

	public void setContentForeColor(final String contentForeColor) {
		this.contentForeColor = contentForeColor;
	}

}
